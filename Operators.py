
"""Python operators are special symbols or keywords that perform operations on operands. Operands can be variables, values, or expressions. Here are some common types of operators in Python:

Arithmetic Operators:

+ (Addition)
- (Subtraction)
* (Multiplication)
/ (Division)
% (Modulus - returns the remainder)
// (Floor Division - returns the quotient as an integer)
** (Exponentiation)

a = 10
b = 3
print(a + b)  # 13
print(a / b)  # 3.3333333333333335
print(a // b) # 3
print(a % b)  # 1


Comparison Operators:

== (Equal to)
!= (Not equal to)
< (Less than)
> (Greater than)
<= (Less than or equal to)
>= (Greater than or equal to)

x = 5
y = 10
print(x == y)  # False
print(x < y)   # True


Logical Operators:

and (Logical AND)
or (Logical OR)
not (Logical NOT)

p = True
q = False
print(p and q)  # False
print(p or q)   # True
print(not p)    # False


Assignment Operators:

= (Assignment)
+= (Addition assignment)
-= (Subtraction assignment)
*= (Multiplication assignment)
/= (Division assignment)
%= (Modulus assignment)
//= (Floor division assignment)
**= (Exponentiation assignment)

x = 5
x += 3  # equivalent to x = x + 3
print(x)  # 8


Identity Operators:

is (True if the operands are identical)
is not (True if the operands are not identical)

a = [1, 2, 3]
b = [1, 2, 3]
print(a is b)    # False
print(a is not b) # True


Membership Operators:

in (True if a value is found in the sequence)
not in (True if a value is not found in the sequence)

my_list = [1, 2, 3, 4]
print(2 in my_list)    # True
print(5 not in my_list) # True"""
